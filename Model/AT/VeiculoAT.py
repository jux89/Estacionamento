class VeiculoAT(object):

    def __init__(self):
        self.Cod = []
        self.Marca = []
        self.Modelo = []
        self.AnoFabricacao = []
        self.AnoModelo = []
        self.Chassi = []
        self.Placa = []

    @property
    def Cod(self):
        return self.__Cod

    @Cod.setter
    def Cod(self):
        self.__Cod = self.Cod

    @property
    def Marca(self):
        return self.__Marca

    @Marca.setter
    def Marca(self):
        self.__Marca = self.__Marca

    @property
    def Modelo(self):
        return self.__Modelo

    @Modelo.setter
    def Modelo(self):
        self.__Modelo = self.__Modelo

    @property
    def AnoFabricacao(self):
        return self.__AnoFabricacao

    @AnoFabricacao.setter
    def AnoFabricacao(self):
        self.__AnoFabricacao = self.__AnoFabricacao

    @property
    def AnoModelo(self):
        return self.__AnoModelo

    @AnoModelo.setter
    def AnoModelo(self):
        self.__AnoModelo = self.__AnoModelo

    @property
    def Chassi(self):
        return self.__Chassi

    @Chassi.setter
    def Chassi(self):
        self.__Chassi = self.Chassi

    @property
    def Placa(self):
        return self.__Placa

    @Placa.setter
    def Placa(self):
        self.__Placa = self.__Placa












